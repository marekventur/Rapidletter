<ol class = "numbered_list faq_list" >
<li id="faq1"><strong>Ist eine Registrierung notwendig um die Funktionalität von Rapidletter.de nutzen zu können?</strong><br/>
Nein, auch ohne sich zu registrieren können Sie Rapidletter.de in breitem Umfang nutzen. Wenn Sie jedoch wiederholt Briefe mit 
Rapidletter.de erstellen, können Sie mit einer Registrierung den vollen Umfang des Angebots genießen und Briefe noch komfortabler 
generieren.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq2"><strong>Welche Vorteile bringt mir eine Registrierung?</strong><br/>
Mit der Registrierung bei Rapidletter.de können Sie jederzeit auf Ihre eigenen Tempaltes zurückgreifen. Sie können Ihre 
Absenderadresse automatidsch übernehmen und Ihr eigenes Adressbuch verwalten. Desweiteren haben Sie die Möglichkeit immer 
wiederkehrende Briefe zu speichern. Das Erstellen eines Briefes wird so noch schneller, noch effizienter, noch einfacher.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq3"><strong>Wo kann ich mich registrieren?</strong><br/>
Um sich zu registrieren, klicken Sie rechts oben auf 'Login'. Dort finden Sie den Button 'kostenlosen Account Anlegen'.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq4"><strong>MBekomme ich bei der Registrierung eine Bestätigungsmail von Rapidletter.de?</strong><br/>
Nein, Sie erhalten keine weitere E-Mails zum Bestätigen, sondern werden sofort freigeschaltet.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq5"><strong>Was passiert mit meinen persönlichen Daten?</strong><br/>
Ihre Daten werden nur zur Erstellung Ihrer persönlichen Briefe gespeichert. Es findet keine weitere Verwendung oder Auswertung der 
Daten statt.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq6"><strong>Wie kann ich einen Brief anlegen?</strong><br/>
Starten Sie den Briefgenerator auf der Hauptseite, dieser wird Sie durch die einzelnen Schritte führen. Wenn Sie mit der Maus über 
die auszufüllenden Felder fahren, wird häufig eine Kurzinformation eingeblendet.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq7"><strong>Kann ich mich auf die Richtigkeit der Vorlagen verlassen?</strong><br/>
Nein, Rapidletter.der übernimmt keiner Gewähr für die Richtigkeit der Vorlagen. Diese dienen lediglich als unverbindliche 
Hilfestellung. Es liegt in der Verantwortung eines jeden Nutzers die Vorlagen auf Richtigkeit und Zweckmäßigkeit zu prüfen.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq8"><strong>Was sind Pflichtangaben bei dem Erstellen eines Briefes?</strong><br/>
Systembedingt werden alle Felder als optional gesehen, um eine größtmögliche Flexibilität für verschiedene Benutzergruppen zu 
gewährleisten. Eingaben wie Straße bzw. Postfach,  Postleitzahl und Ort bzw. Stadt sollten jedoch ausgefüllt werden.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq9"><strong>Was ist der Unterschied zwischen einem privaten und einem geschäftlichen Brief?</strong><br/>
Geschäftsbriefe müssen die gesetzlich geforderten Pflichtangaben entsprechend der jeweiligen Gesellschaftsform führen. Im Verlauf 
der Einzelnen Schritte, werden dafür entsprechende Eingabefelder zur Verfügung gestellt, die bei der Erstellung eines Privatbriefes 
nicht notwendig sind.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq10"><strong>Was bedeutet "ein Brief als Vorlage veröffentlichen"?</strong><br/>
Hiermit kann man eine Briefvorlage als Link per E-Mail oder über Netzwerke veröffentlichen. Unterstützt werden Life-Stream-Einträge 
bei Facebook, vorformulierte Tweets für Twitter, Pinnwaneinträge bei StudiVZ sowie Bookmark-Links für mister-wong.de, delicious.com, 
stumbleupon.com und Google Bookmarks. Veröffentlichte Briefe können somit als hilfreiche Vorlage bei öfters auftretenden Fällen oder 
Briefaktionen innerhalb der unterstützten Communities genutzt werden.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq11"><strong>Warum wird mein Logo wird nicht in die Briefvorlage übernommen?</strong><br/>
Nachdem Sie ein Logo aus Ihrem Verzeichnis durch klick auf den Button 'Durchsuchen' ausgewählt haben, müssen Sie links auf 'Vorschau 
aktualisieren' klicken. Vergewissern Sie sich, dass Ihre Bilddatei eine Dateigröße von 2 Megabyte nicht überschreitet.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>

<li id="faq12"><strong>Ich möchte mein Brief gerne in einem andern Format als *.pdf speichern!</strong><br/>
Eine Speicherung der Briefe in einem anderen Format, wie zum Beispiel *.doc ist leider nicht möglich. Es wird ausschließlich 
das *.pdf Format unterstützt.<br/>
<a href="#topfaq" class="gray">zurück zum Seitenanfang</a></li>
